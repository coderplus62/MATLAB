clear
clc

% default, displays 4 decimal places
format short 
x = pi;
disp(x)

% displays more decimal places
format long 
x = pi;
disp(x)

format short e % exponential notation, 4 decimal places
x = pi;
disp(x)

format long e % exponential notation, more decimal places
x = pi;
disp(x)

format short g % displays more decimal places
x = pi;
disp(x)

format short Eng % engineering notation, 4 decimal places
x = pi;
disp(x)

format default
